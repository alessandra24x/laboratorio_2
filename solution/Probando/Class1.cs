using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Probando
{
    class Operacion
    {
        public static int Sumar(int x1, int x2)
        {
            int s = x1 + x2;
            return s;
        }

        public static int Restar(int x1, int x2)
        {
            int r = x1 - x2;
            return r;
        }
    }
}
